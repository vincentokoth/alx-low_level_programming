/**                                                                                                                                           *_strlen-Leng of a string
 *@s: char
 *return: int 
 *function that returns the length of a string.
 *
 */

int _strlen(char *s)
{
    int a;
    
    for (a = 0; s[a] != '\0'; a++)
      {
	continue;
      }
    return(a);
}
