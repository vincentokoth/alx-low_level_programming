/**
 * _strlen - a function that returns the length of a string.
 * @s: string whose lenght is to be returned.
 * Return:int
 */
int _strlen(char *s)
{
	int a;

	for (a = 0; s[a] != '\0'; a++)
	{
	continue;
	}

	return (a);
}
